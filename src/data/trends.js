function generateHistory(baseValue, volatility, trendDirection) {
  const history = [];
  let value = baseValue;
  for (let i = 29; i >= 0; i--) {
    const date = new Date();
    date.setDate(date.getDate() - i);
    const noise = (Math.random() - 0.5) * volatility;
    const drift = trendDirection * (30 - i) * 0.3;
    value = Math.max(0, Math.min(100, baseValue + drift + noise));
    history.push({
      day: date.toISOString().split('T')[0],
      value: Math.round(value * 10) / 10,
    });
  }
  return history;
}

